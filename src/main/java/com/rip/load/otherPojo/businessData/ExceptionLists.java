package com.rip.load.otherPojo.businessData;

/**
 *企业异常名录
 */
public class ExceptionLists {

    /**
     * 企业名称
     */
    private String name;

    /**
     * 企业类型
     */
    private String type;

    /**
     * 列入日期
     */
    private String inDate;

    /**
     * 列入原因
     */
    private String inReason;

    /**
     * 移出日期
     */
    private String outDate;

    /**
     * 退出异常名录原因
     */
    private String outReason;

    /**
     * 注册号
     */
    private String regNo;

    /**
     * 统一社会信用代码
     */
    private String shxydm;

    /**
     * 列入机关名称
     */
    private String inOrg;

    /**
     * 移出机关名称
     */
    private String outOrg;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getInDate() {
        return inDate;
    }

    public void setInDate(String inDate) {
        this.inDate = inDate;
    }

    public String getInReason() {
        return inReason;
    }

    public void setInReason(String inReason) {
        this.inReason = inReason;
    }

    public String getOutDate() {
        return outDate;
    }

    public void setOutDate(String outDate) {
        this.outDate = outDate;
    }

    public String getOutReason() {
        return outReason;
    }

    public void setOutReason(String outReason) {
        this.outReason = outReason;
    }

    public String getRegNo() {
        return regNo;
    }

    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }

    public String getShxydm() {
        return shxydm;
    }

    public void setShxydm(String shxydm) {
        this.shxydm = shxydm;
    }

    public String getInOrg() {
        return inOrg;
    }

    public void setInOrg(String inOrg) {
        this.inOrg = inOrg;
    }

    public String getOutOrg() {
        return outOrg;
    }

    public void setOutOrg(String outOrg) {
        this.outOrg = outOrg;
    }
}
