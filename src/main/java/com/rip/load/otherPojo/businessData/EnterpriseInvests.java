    public String getConFrom() {
        return conFrom;
    }

    public void setConFrom(String conFrom) {
        this.conFrom = conFrom;
    }

    public String getFundedRation() {
        return fundedRation;
    }

    public void setFundedRation(String fundedRation) {
        this.fundedRation = fundedRation;
    }

    public String getBinvvAmount() {
        return binvvAmount;
    }

    public void setBinvvAmount(String binvvAmount) {
        this.binvvAmount = binvvAmount;
    }

