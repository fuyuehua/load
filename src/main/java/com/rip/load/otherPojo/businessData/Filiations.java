package com.rip.load.otherPojo.businessData;
/**
 *分支机构信息
 */
public class Filiations {

    /**
     *分支机构名称
     */
    private String brName;

    /**
     *分支机构企业注册号
     */
    private String brRegNo;

    /**
     * 分支机构负责人
     */
    private String brPrincipal;

    /**
     *一般经营项目
     */
    private String cbuItme;

    /**
     *分支机构地址
     */
    private String brAddr;

    /**
     *分支机构统一社会信用代码
     */
    private String brnCreditCode;

    /**
     *分支机构登记机关
     */
    private String brnRegOrg;

    /**
     *分支机构主体身份代码
     */
    private String brpripId;


    public String getBrName() {
        return brName;
    }

    public void setBrName(String brName) {
        this.brName = brName;
    }

    public String getBrRegNo() {
        return brRegNo;
    }

    public void setBrRegNo(String brRegNo) {
        this.brRegNo = brRegNo;
    }

    public String getBrPrincipal() {
        return brPrincipal;
    }

    public void setBrPrincipal(String brPrincipal) {
        this.brPrincipal = brPrincipal;
    }

    public String getCbuItme() {
        return cbuItme;
    }

    public void setCbuItme(String cbuItme) {
        this.cbuItme = cbuItme;
    }

    public String getBrAddr() {
        return brAddr;
    }

    public void setBrAddr(String brAddr) {
        this.brAddr = brAddr;
    }

    public String getBrnCreditCode() {
        return brnCreditCode;
    }

    public void setBrnCreditCode(String brnCreditCode) {
        this.brnCreditCode = brnCreditCode;
    }

    public String getBrnRegOrg() {
        return brnRegOrg;
    }

    public void setBrnRegOrg(String brnRegOrg) {
        this.brnRegOrg = brnRegOrg;
    }

    public String getBrpripId() {
        return brpripId;
    }

    public void setBrpripId(String brpripId) {
        this.brpripId = brpripId;
    }
}
