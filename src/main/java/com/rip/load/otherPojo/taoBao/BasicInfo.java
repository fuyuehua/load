package com.rip.load.otherPojo.taoBao;
/**
 *用户基本信息
 */
public class BasicInfo {
    /**
     *用户名
     */
    private String username;

    /**
     *昵称
     */
    private String nickName;

    /**
     *性别
     */
    private String gender;
    /**
     *出生日期
     */
    private String birthday;

    /**
     *真实姓名
     */
    private String realName;

    /**
     *身份证
     */
    private String identityNo;

    /**
     *认证渠道
     */
    private String identityChannel;

    /**
     *是否实名认证（已认证、未认证）
     */
    private String identityStatus;

    /**
     *登录邮箱
     */
    private String email;

    /**
     *绑定手机
     */
    private String mobile;

    /**
     *会员等级
     */
    private String vipLevel;

    /**
     *成长值
     */
    private String growthValue;

    /**
     *信用积分
     */
    private String creditScore;
    /**
     *好评率
     */
    private String favorableRate;


    /**
     *安全等级
     */
    private String securityLevel;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getIdentityNo() {
        return identityNo;
    }

    public void setIdentityNo(String identityNo) {
        this.identityNo = identityNo;
    }

    public String getIdentityChannel() {
        return identityChannel;
    }

    public void setIdentityChannel(String identityChannel) {
        this.identityChannel = identityChannel;
    }

    public String getIdentityStatus() {
        return identityStatus;
    }

    public void setIdentityStatus(String identityStatus) {
        this.identityStatus = identityStatus;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getVipLevel() {
        return vipLevel;
    }

    public void setVipLevel(String vipLevel) {
        this.vipLevel = vipLevel;
    }

    public String getGrowthValue() {
        return growthValue;
    }

    public void setGrowthValue(String growthValue) {
        this.growthValue = growthValue;
    }

    public String getCreditScore() {
        return creditScore;
    }

    public void setCreditScore(String creditScore) {
        this.creditScore = creditScore;
    }

    public String getFavorableRate() {
        return favorableRate;
    }

    public void setFavorableRate(String favorableRate) {
        this.favorableRate = favorableRate;
    }

    public String getSecurityLevel() {
        return securityLevel;
    }

    public void setSecurityLevel(String securityLevel) {
        this.securityLevel = securityLevel;
    }
}
