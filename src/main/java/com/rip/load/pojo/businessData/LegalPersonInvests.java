package com.rip.load.pojo.businessData;

/**
 * 法人对外投资信息
 */
public class LegalPersonInvests {

    /**
     * 姓名
     */
    private String name;

    /**
     * 企业(机构)名称
     */
    private String entName;

    /**
     *注册号
     */
    private String regNo;

    /**
     *企业(机构)类型
     */
    private String entType;

    /**
     *注册资本(万元)
     */
    private String regCap;

    /**
     *注册资本币种
     */
    private String regCapCur;

    /**
     *经营状态（在营、吊销、注销、其他）
     */
    private String entStatus;

    /**
     *注销日期
     */
    private String canDate;
    /**
     *吊销日期
     */
    private String revDate;

    /**
     *登记机关
     */
    private String regOrg;

    /**
     *认缴出资额(万元)
     */
    private String subConAm;

    /**
     *认缴出资币种
     */
    private String curRenCy;
    /**
     *出资方式
     */
    private String conForm;

    /**
     *出资比例
     */
    private String fundedRatio;

    /**
     * 开业日期
     */
    private String esDate;

    /**
     *注册地址行政区编号
     */
    private String postCode;
    /**
     *企业总数量
     */
    private String entAmount;

    /**
     *信用号
     */
    private String creditNo;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEntName() {
        return entName;
    }

    public void setEntName(String entName) {
        this.entName = entName;
    }

    public String getRegNo() {
        return regNo;
    }

    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }

    public String getEntType() {
        return entType;
    }

    public void setEntType(String entType) {
        this.entType = entType;
    }

    public String getRegCap() {
        return regCap;
    }

    public void setRegCap(String regCap) {
        this.regCap = regCap;
    }

    public String getRegCapCur() {
        return regCapCur;
    }

    public void setRegCapCur(String regCapCur) {
        this.regCapCur = regCapCur;
    }

    public String getEntStatus() {
        return entStatus;
    }

    public void setEntStatus(String entStatus) {
        this.entStatus = entStatus;
    }

    public String getCanDate() {
        return canDate;
    }

    public void setCanDate(String canDate) {
        this.canDate = canDate;
    }

    public String getRevDate() {
        return revDate;
    }

    public void setRevDate(String revDate) {
        this.revDate = revDate;
    }

    public String getRegOrg() {
        return regOrg;
    }

    public void setRegOrg(String regOrg) {
        this.regOrg = regOrg;
    }

    public String getSubConAm() {
        return subConAm;
    }

    public void setSubConAm(String subConAm) {
        this.subConAm = subConAm;
    }

    public String getCurRenCy() {
        return curRenCy;
    }

    public void setCurRenCy(String curRenCy) {
        this.curRenCy = curRenCy;
    }

    public String getConForm() {
        return conForm;
    }

    public void setConForm(String conForm) {
        this.conForm = conForm;
    }

    public String getFundedRatio() {
        return fundedRatio;
    }

    public void setFundedRatio(String fundedRatio) {
        this.fundedRatio = fundedRatio;
    }

    public String getEsDate() {
        return esDate;
    }

    public void setEsDate(String esDate) {
        this.esDate = esDate;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getEntAmount() {
        return entAmount;
    }

    public void setEntAmount(String entAmount) {
        this.entAmount = entAmount;
    }

    public String getCreditNo() {
        return creditNo;
    }

    public void setCreditNo(String creditNo) {
        this.creditNo = creditNo;
    }
}
